
public class Main {

	public static void main(String[] args) {
		//0- 100 çift sayılar
		System.out.print("Even numbers:");
		for(int i=0;i<=100;i+=2)
			System.out.print(i+" ");
			
		System.out.print("\nOdd numbers:");
		for(int i=1;i<=100;i+=2)
			System.out.print(i+" ");
		
		System.out.print("\n5  numbers :");
		for(int i=0;i<=100;i+=5)
			System.out.print(i+" ");

	}

}
